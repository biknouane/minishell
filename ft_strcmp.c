#include "shell.h"

int	ft_strcmp(char *str1, char *str2)
{
	if (!*str1 || !*str2)
		return (0);
	while (*str1)
	{
		if (*str1 != *str2)
			return (0);
		str1++;
		str2++;
	}
	return (1);
}
